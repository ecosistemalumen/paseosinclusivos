async function main() {
    console.log('Start seeding ...');
    for (const lugar of LUGARES) {
        const existing = await prisma.lugar.findUnique({
            where: { slug: lugar.slug }
        });

        if (!existing) {
            const result = await prisma.lugar.create({
                data: lugar,
            });
            console.log(`Created lugar: ${result.nombre}`);
        } else {
            console.log(`Lugar already exists: ${lugar.nombre}`);
        }
    }
    console.log('Seeding finished.');
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
